            driver.Navigate().GoToUrl("http://localhost/MiTAP.AdminWebSite/TestCase/Edit/950440?ProjectID=1065&GroupName=minerets%20group&GroupID=98346&ProjectName=JinkenTest20");
            driver.FindElement(By.Id("summary")).Click();
